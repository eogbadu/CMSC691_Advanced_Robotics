# Clean and process the passed in sentence
def processSentence(text):
    text = removeBrackets(text)
    text = re.sub("[^a-zA-Z]"," ", text)
    my_list =  text.lower().split()
    my_list = [w for w in my_list if not w in STOPWORDS]

    return my_list

# Remove brackets and the words within the brackets
def removeBrackets(text, brackets="<>"):
    count = [0] * (len(brackets) // 2)
    saved_chars = []
    for character in text:
        for i, b in enumerate(brackets):
            if character == b: 
                kind, is_close = divmod(i, 2)
                count[kind] += (-1)**is_close 
                if count[kind] < 0: 
                    count[kind] = 0  
                else: 
                    break
        else:
            if not any(count):
                saved_chars.append(character)
    return ''.join(saved_chars)



